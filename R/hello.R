#' Function to add two numbers
#'
#'@param x First number
#'@param n Second number
#'
#'
#' @return a number
#' @export
#'
#' @examples
#' plus_n(2,3)
#' plus_n(2,3)
plus_n <- function(x,n) {
  return(x + n)
}

#' Function to multiply numbers
#'
#' @param x First number
#' @param n Second number
#'
#' @return a number
#' @export
#'
#' @examples
#' multiply_n(2,3)
#' multiply_n(2,3)
multiply_n <-function(x,n) {
  return(x*n)
}
